/**
 * Get service type label by value
 */
export const getServiceTypeLabel = (value: string): string => {
  const serviceType = SERVICE_TYPES.find(st => st.value === value)
  return serviceType ? serviceType.label : value
}

/**
 * Format service type for display: "1 - Medical Care"
 */
export const formatServiceType = (value: string): string => {
  const serviceType = SERVICE_TYPES.find(st => st.value === value)
  return serviceType ? `${serviceType.value} - ${serviceType.label}` : value
}

